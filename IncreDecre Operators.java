package com.company;

public class IncreDecre{
    public static void main(String[] args){

        // Increment Operators
        int a = 10;
        System.out.println(a++); // Increment after 
        System.out.println(a);
        System.out.println(++a); // Increment Befor
        System.out.println(a);

        // Decrement Operators
        int b = 10;
        System.out.println(b--); // Decrement after 
        System.out.println(b);
        System.out.println(--b); // Decrement Befor
        System.out.println(b);
    }
}